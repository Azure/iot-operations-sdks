#!/usr/bin/env bash

# DESCRIPTION: Runs static analysis for Git-tracked Rust files reachable
# from the current directory:
# - Rust sources MUST end with a newline
# - Rust sources MUST include the Microsoft copyright header
# - Rust crate manifest files MUST contain:
#     [lints]
#     workspace = true
# - Rust sources MUST be formatted with `rustfmt`
# - The workspace MUST pass `cargo clippy -- --deny=warnings`
# - The workspace MUST have no unused direct dependencies (`cargo machete`)
#
# PARAMETERS: NONE

set -euo pipefail

CURDIR="$(pwd -P)"

# NOTE: Null-terminated strings are used for filenames; unset IFS.
IFS=

# Require ending newlines.
git ls-files -z -- '*.rs' \
| while read -r -d '' FILE; do
    if [ -n "$(tail -c 1 "${FILE}")" ]; then
        echo "ERROR: ${FILE} is missing a trailing newline" >&2
        exit 1
    fi
done

# Require copyright headers.
git ls-files -z -- '*.rs' \
| while read -r -d '' FILE; do
    # Skip files generated by the protocol compiler.
    if head -n 1 "${FILE}" | grep -q 'Code generated by Azure.Iot.Operations.ProtocolCompiler'; then
        continue
    fi

    HEADER_LINE_1="$(sed -n '1p' "${FILE}")"
    HEADER_LINE_2="$(sed -n '2p' "${FILE}")"

    if [ "${HEADER_LINE_1}" != "// Copyright (c) Microsoft Corporation." ] \
       || [ "${HEADER_LINE_2}" != "// Licensed under the MIT License." ]; then
        echo "ERROR: ${FILE} is missing the Microsoft copyright header" >&2
        exit 1
    fi
done

# Require `[lints] workspace = true` in every workspace manifest.
cargo metadata --format-version=1 --no-deps \
| jq -r '.packages[].manifest_path | select(startswith($cwd))' \
    --arg cwd "${CURDIR}" \
| while read -r FILE; do
    # Skip generated files.
    if head -n 1 "${FILE}" | grep -q 'Code generated by Azure.Iot.Operations.ProtocolCompiler'; then
        continue
    fi
    if ! grep -q $'^\[lints\]\nworkspace = true$' "${FILE}"; then
        echo "ERROR: ${FILE} is missing [lints] workspace = true" >&2
        exit 1
    fi
done

# Require `rustfmt` pass.
cargo fmt --verbose --all --check

# Require clippy pass.
cargo clippy --all --all-features --tests --examples -- --deny=warnings

# Check for unused dependencies.
cargo machete
